-- Doctor profile extensions and consultation work orders.

alter table doctor
  add column if not exists user_id bigint references sys_user(id) on delete set null,
  add column if not exists license_no varchar(128),
  add column if not exists profile_md text,
  add column if not exists consult_status varchar(32) not null default 'online',
  add column if not exists audit_status varchar(32) not null default 'approved',
  add column if not exists reviewed_by bigint references sys_user(id) on delete set null,
  add column if not exists reviewed_at timestamp;

do $$
declare
  v_constraint_name text;
begin
  select c.conname
    into v_constraint_name
  from pg_constraint c
  join pg_class t on t.oid = c.conrelid
  where t.relname = 'doctor'
    and c.contype = 'c'
    and pg_get_constraintdef(c.oid) like '%consult_status%'
  limit 1;

  if v_constraint_name is not null then
    execute 'alter table doctor drop constraint ' || quote_ident(v_constraint_name);
  end if;
end $$;

alter table doctor
  add constraint doctor_consult_status_check
  check (consult_status in ('online', 'offline', 'paused'));

